// /backend/models/Lead.js
import mongoose from "mongoose";

const leadModel = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true,
    },
    phone: {
        type: String,
        required: true,
        trim: true,
    },
    email: {
        type: String,
        required: true,
        trim: true,
        unique: true,
        lowercase: true,
    },
    loanType: {
        type: String,
        required: true,
        enum: ["personal", "business", "mortgage", "auto", "student", "other"],
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    },
});

export default mongoose.model("Lead", leadModel);
